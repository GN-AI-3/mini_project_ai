    result = text
    
    # 문장 끝 처리
    for formal, casual in ending_rules.items():
        if result.endswith(formal):
            result = result[:-len(formal)] + casual
            break
        if result.endswith(formal + "."):
            result = result[:-len(formal) - 1] + casual
            break
    
    # 중간 문장 처리
    for formal, casual in formal_to_casual.items():
        result = result.replace(formal, casual)
    
    return result

